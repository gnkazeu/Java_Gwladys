package JavaAss1;

public class Swap {
    public static void main (String[] args) {

        int a = 10;
        int b = 20;

        System.out.println("New value of a is " + a);
        System.out.println("New value of b is " + b);

        int c = a;
        a = b;
        b = c; 

        System.out.println("New value of a is " + a);
        System.out.println("New value of b is " + b);
    
    }
    
}
